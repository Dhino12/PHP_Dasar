<?php 

echo "==== Increment ====<br>\n";
$a = 10;
echo $a . "<br> \n";

// Post Increment
$b = $a++;
echo $b . "<br> \n";

// Pre Increment
$b = ++$a;
echo $b . "<br> \n";

echo "==== Decrement ====<br>\n";

$c = 20;
echo $c . "<br> \n";

$d = $c--;
echo $d . "<br> \n";

$d = --$c;
echo $d . "<br> \n";